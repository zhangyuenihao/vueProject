<template>
    <div class="tab-control">
        <div class="tabcontrol-item"
             v-for="(item,index) in titles"
             :key="index"
             :class="{active:index===currentIndex}"
             @click="itemClick(index)">
            <span>{{item}}</span>
        </div>
    </div>
</template>

<script>
    export default {
        name: "TabControl",
        props: {
            titles: {
                type: Array,
                default() {
                    return []
                }
            }
        },
        data() {
            return {
                currentIndex: 0
            }
        },
        methods: {
            itemClick(index) {
                this.currentIndex = index
                this.$emit('tabClick',index)
            }


        }
    }
</script>

<style scoped>
    .tab-control {
        display: flex;
        height: 40px;
        line-height: 40px;
        text-align: center;
    }

    .tabcontrol-item {
        flex: 1;
        cursor: pointer;
    }
    .active{
        color: var(--color-high-text);
    }
    .active span{
        border-bottom: 1px solid var(--color-high-text);
        padding: 5px;
    }
</style>